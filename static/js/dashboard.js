let currentCharacter = null;
let currentCharacterKey = null;
let chatMessages = [];
let currentTheme = localStorage.getItem('theme') || 'dark';
let isSidebarCollapsed = localStorage.getItem('sidebarCollapsed') === 'true';
let customLumos = JSON.parse(localStorage.getItem('customLumos') || '[]');

// --- VOICE (STT & TTS) STATE ---
let recognition; 
let synth = window.speechSynthesis;
let isListening = false;

// --- SPEECH FUNCTIONS ---

function initSTT() {
    const SpeechRecognition = window.SpeechRecognition || window.webkitSpeechRecognition;
    if (!SpeechRecognition) return;

    recognition = new SpeechRecognition();
    recognition.continuous = false;
    recognition.lang = 'en-US';
    recognition.interimResults = false;

    recognition.onresult = (event) => {
        const transcript = event.results[0][0].transcript;
        const input = getEl('chatInput');
        if (input) input.value = transcript;
        toggleMicUI(false);
    };

    recognition.onerror = () => toggleMicUI(false);
    recognition.onend = () => toggleMicUI(false);
}

function toggleMicUI(listening) {
    isListening = listening;
    const micBtn = getEl('micBtn');
    if (micBtn) {
        if (listening) {
            micBtn.classList.add('listening');
            micBtn.textContent = '🛑';
        } else {
            micBtn.classList.remove('listening');
            micBtn.textContent = '🎤';
        }
    }
}

function startListening() {
    if (!recognition) return;
    if (isListening) {
        recognition.stop();
    } else {
        recognition.start();
        toggleMicUI(true);
    }
}

function speakText(text) {
    stopSpeaking(); // Cancel any current speech before starting new
    
    // Clean text: strip emojis and markdown stars for smoother speech
    const cleanText = text.replace(/[\u{1F600}-\u{1F64F}\u{1F300}-\u{1F5FF}\u{1F680}-\u{1F6FF}\u{2600}-\u{26FF}\u{2700}-\u{27BF}]/gu, '')
                          .replace(/\*/g, '');

    const utterance = new SpeechSynthesisUtterance(cleanText);
    utterance.rate = 1.0;
    
    const stopBtn = getEl('stopVoiceBtn');
    utterance.onstart = () => { if (stopBtn) stopBtn.style.filter = "hue-rotate(150deg)"; };
    utterance.onend = () => { if (stopBtn) stopBtn.style.filter = "none"; };

    synth.speak(utterance);
}

function stopSpeaking() {
    if (synth.speaking) {
        synth.cancel();
    }
}

// --- DASHBOARD CORE LOGIC ---

function setTheme(theme) {
    currentTheme = theme;
    localStorage.setItem('theme', theme);
    document.body.className = theme === 'dark' ? 'dark-mode' : 'light-mode';

    const toggleBtn = getEl('themeToggle');
    if (toggleBtn) {
        toggleBtn.textContent = theme === 'dark' ? '🌙' : '☀️';
    }
}

function toggleTheme() {
    setTheme(currentTheme === 'dark' ? 'light' : 'dark');
}

function setSidebarState(collapsed) {
    isSidebarCollapsed = collapsed;
    localStorage.setItem('sidebarCollapsed', collapsed);

    const sidebar = getEl('sidebar');
    if (sidebar) {
        sidebar.classList.toggle('collapsed', collapsed);
    }
}

function toggleSidebar() {
    setSidebarState(!isSidebarCollapsed);
}

function slugify(text) {
    return text.toLowerCase()
        .replace(/[^a-z0-9]+/g, '-')
        .replace(/^-+|-+$/g, '');
}

function saveCustomLumos() {
    localStorage.setItem('customLumos', JSON.stringify(customLumos));
}

function buildCustomCharacterKey(name) {
    const base = slugify(name) || 'custom-lumo';
    let key = base;
    let counter = 1;

    while (characterData[key]) {
        key = `${base}-${counter}`;
        counter += 1;
    }

    return key;
}

function createCharacterCard(characterKey, character) {
    const container = document.querySelector('.characters-grid');
    if (!container) return;

    const card = document.createElement('div');
    card.className = 'character-card custom-card';
    card.dataset.character = characterKey;
    card.dataset.category = character.category || 'custom';

    card.innerHTML = `
      <div class="card-overlay">
        <h3>${character.name}</h3>
        <span class="mini-role">${character.role}</span>
        <div class="card-tags">
          ${character.tags.map(tag => `<span>${tag}</span>`).join('')}
        </div>
        <div class="card-stats">💬 Custom</div>
        <p>${character.description}</p>
      </div>
    `;

    card.addEventListener('click', () => openChat(characterKey, card));
    container.prepend(card);
}

function loadCustomLumos() {
    customLumos.forEach(lumo => {
        const key = buildCustomCharacterKey(lumo.name);
        characterData[key] = {
            name: lumo.name,
            avatar: lumo.avatar || '✨',
            role: lumo.role,
            category: lumo.category || 'custom',
            tags: lumo.tags || ['✨'],
            description: lumo.description || 'A custom Lumo created by you.'
        };
        createCharacterCard(key, characterData[key]);
    });
}

function openCreateModal() {
    const modal = getEl('createModal');
    if (modal) modal.classList.add('active');
}

function closeCreateModal() {
    const modal = getEl('createModal');
    if (modal) modal.classList.remove('active');
}

async function createCustomLumo(event) {
    event.preventDefault();

    const nameInput = getEl('customName');
    const roleInput = getEl('customRole');
    const promptInput = getEl('customPrompt');
    const tagsInput = getEl('customTags');

    if (!nameInput || !roleInput || !promptInput) return;

    const name = nameInput.value.trim();
    const role = roleInput.value.trim();
    const prompt = promptInput.value.trim();
    const tags = tagsInput.value.split(',').map(tag => tag.trim()).filter(Boolean);

    if (!name || !role || !prompt) {
        alert('Please fill in the name, role, and prompt to create your Lumo.');
        return;
    }

    const key = buildCustomCharacterKey(name);
    const description = prompt.length > 120 ? `${prompt.slice(0, 117)}...` : prompt;

    const newLumo = {
        name,
        avatar: '✨',
        role,
        category: 'custom',
        tags: tags.length ? tags : ['✨'],
        description,
        prompt
    };

    characterData[key] = newLumo;
    customLumos.push(newLumo);
    saveCustomLumos();
    createCharacterCard(key, newLumo);

    nameInput.value = '';
    roleInput.value = '';
    promptInput.value = '';
    tagsInput.value = '';

    closeCreateModal();
}

function setText(id, value) {
    const el = document.getElementById(id);
    if (el) el.textContent = value;
}

function getEl(id) {
    return document.getElementById(id);
}

function escapeHTML(str) {
    if (typeof str !== 'string') return str;
    return str
        .replace(/&/g, '&amp;')
        .replace(/</g, '&lt;')
        .replace(/>/g, '&gt;')
        .replace(/"/g, '&quot;')
        .replace(/'/g, '&#39;');
}

function formatChatText(text) {
    const safeText = escapeHTML(text);
    return safeText.replace(/\*(.*?)\*/g, '<span class="emphasis-text">$1</span>');
}

function disableChatInput(disabled) {
    const input = getEl('chatInput');
    const sendBtn = getEl('sendBtn');
    const micBtn = getEl('micBtn');

    if (input) input.disabled = disabled;
    if (sendBtn) sendBtn.disabled = disabled;
    if (micBtn) micBtn.disabled = disabled;
}

function buildHistoryForAPI() {
    return chatMessages
        .filter(msg => msg.content !== "Typing...")
        .map(msg => ({
            role: msg.role,
            content: msg.content
        }));
}

async function loadUser() {
    try {
        const response = await fetch('/api/user');
        if (response.ok) {
            const user = await response.json();
            setText('user-name', `Welcome, ${user.name || user.email}!`);
        }
    } catch (error) {
        console.log('User load failed:', error);
    }
}

function openChat(characterKey, cardElement = null) {
    currentCharacter = characterData[characterKey];
    currentCharacterKey = characterKey;

    if (!currentCharacter) return;

    document.querySelectorAll('.character-card').forEach(card => {
        card.classList.remove('active-card');
    });

    if (cardElement) {
        cardElement.classList.add('active-card');
    }

    setText('modalAvatar', currentCharacter.avatar);
    setText('modalName', currentCharacter.name);
    setText('modalRole', currentCharacter.role);

    chatMessages = [{
        role: 'assistant',
        content: `Hello! I'm ${currentCharacter.name}. ✨ Ask me anything.`
    }];

    updateChatDisplay();

    const modal = getEl('chatModal');
    if (modal) modal.classList.add('active');

    const input = getEl('chatInput');
    if (input) input.focus();
}

function closeChat() {
    stopSpeaking(); // Shut down voice on close
    const modal = getEl('chatModal');
    if (modal) modal.classList.remove('active');

    currentCharacter = null;
    currentCharacterKey = null;
    chatMessages = [];

    document.querySelectorAll('.character-card').forEach(card => {
        card.classList.remove('active-card');
    });
}

async function sendMessage() {
    const input = getEl('chatInput');
    const message = input?.value.trim();

    if (!message || !currentCharacter || !currentCharacterKey) return;

    chatMessages.push({
        role: 'user',
        content: message
    });

    if (input) input.value = '';
    updateChatDisplay();

    chatMessages.push({
        role: 'assistant',
        content: 'Typing...'
    });

    updateChatDisplay();
    disableChatInput(true);

    const sendBtn = getEl('sendBtn');
    if (sendBtn) sendBtn.classList.add('loading');

    try {
        const response = await fetch('/api/chat', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({
                message: message,
                character: currentCharacterKey,
                history: buildHistoryForAPI()
            })
        });

        const data = await response.json();

        if (chatMessages.length && chatMessages[chatMessages.length - 1].content === 'Typing...') {
            chatMessages.pop();
        }

        if (response.ok && data.success) {
            const reply = data.answer || "No response received.";
            chatMessages.push({
                role: 'assistant',
                content: reply
            });
            // Trigger Voice Output
            speakText(reply);
        } else {
            chatMessages.push({
                role: 'assistant',
                content: data.error || "Sorry, I couldn't respond right now."
            });
        }

        updateChatDisplay();

    } catch (error) {
        console.error('Chat error:', error);
        if (chatMessages.length && chatMessages[chatMessages.length - 1].content === 'Typing...') {
            chatMessages.pop();
        }
        chatMessages.push({ role: 'assistant', content: "Network error. Please try again." });
        updateChatDisplay();
    } finally {
        disableChatInput(false);
        input?.focus();
        if (sendBtn) sendBtn.classList.remove('loading');
    }
}

function updateChatDisplay() {
    const messagesContainer = getEl('chatMessages');
    if (!messagesContainer) return;

    messagesContainer.innerHTML = '';

    chatMessages.forEach(msg => {
        const messageDiv = document.createElement('div');
        const content = msg.content || '';

        if (msg.role === 'user') {
            messageDiv.className = 'message user';
            messageDiv.innerHTML = `<span class="message-badge user-badge">🧑</span><span class="message-text">${formatChatText(content)}</span>`;
        } else {
            const avatar = currentCharacter?.avatar || '✨';
            const displayContent = content === "Typing..." ? content : `${avatar} ${content}`;

            messageDiv.className = 'message character';
            messageDiv.innerHTML = `
                <span class="message-badge character-badge">${avatar}</span>
                <span class="message-text">${formatChatText(displayContent)}</span>
            `;
        }

        messagesContainer.appendChild(messageDiv);
    });

    messagesContainer.scrollTop = messagesContainer.scrollHeight;
}

function initFilters() {
    document.querySelectorAll('.filter-pill').forEach(btn => {
        btn.addEventListener('click', () => {
            document.querySelectorAll('.filter-pill').forEach(b => b.classList.remove('active'));
            btn.classList.add('active');

            const label = btn.innerText.toLowerCase().trim();
            let filterKey = label.split('&')[0].trim().split(' ')[0];

            document.querySelectorAll('.character-card').forEach(card => {
                const categories = (card.dataset.category || '').toLowerCase();
                if (label.includes('recommend')) {
                    card.classList.remove('hidden-card');
                } else {
                    const show = categories.includes(filterKey);
                    card.classList.toggle('hidden-card', !show);
                }
            });
        });
    });
}

function initSearch() {
    const searchInput = document.getElementById('searchInput');
    if (!searchInput) return;

    searchInput.addEventListener('input', () => {
        const query = searchInput.value.toLowerCase().trim();

        document.querySelectorAll('.character-card').forEach(card => {
            const text = card.innerText.toLowerCase();
            const matches = text.includes(query);
            card.classList.toggle('hidden-card', !matches);
        });
    });
}

function initDashboard() {
    loadUser();
    setTheme(currentTheme);
    setSidebarState(isSidebarCollapsed);
    loadCustomLumos();
    initSTT(); 

    document.querySelectorAll('.character-card').forEach(card => {
        card.addEventListener('click', () => {
            const characterKey = card.dataset.character;
            openChat(characterKey, card);
        });
    });

    const closeModalBtn = getEl('closeModal');
    const chatModal = getEl('chatModal');
    const createBtn = document.querySelector('.create-btn');
    const createModal = getEl('createModal');
    const closeCreateModalBtn = getEl('closeCreateModal');
    const createForm = getEl('createForm');
    const sidebarToggle = getEl('sidebarToggle');
    const micBtn = getEl('micBtn');
    const stopVoiceBtn = getEl('stopVoiceBtn');

    closeModalBtn?.addEventListener('click', closeChat);
    closeCreateModalBtn?.addEventListener('click', closeCreateModal);
    createBtn?.addEventListener('click', openCreateModal);
    createForm?.addEventListener('submit', createCustomLumo);
    sidebarToggle?.addEventListener('click', toggleSidebar);
    
    micBtn?.addEventListener('click', startListening);
    stopVoiceBtn?.addEventListener('click', stopSpeaking);

    chatModal?.addEventListener('click', (e) => {
        if (e.target.id === 'chatModal') closeChat();
    });

    createModal?.addEventListener('click', (e) => {
        if (e.target.id === 'createModal') closeCreateModal();
    });

    const sendBtn = getEl('sendBtn');
    const chatInput = getEl('chatInput');
    const themeToggle = getEl('themeToggle');

    sendBtn?.addEventListener('click', sendMessage);
    themeToggle?.addEventListener('click', toggleTheme);

    chatInput?.addEventListener('keypress', (e) => {
        if (e.key === 'Enter') sendMessage();
    });

    initFilters();
    initSearch();
    initSidebarNav();
}

function initSidebarNav() {
    const navLinks = document.querySelectorAll('.sidebar-nav a');
    const heroSection = document.querySelector('.hero-section');
    const searchInput = getEl('searchInput');
    const filterSection = document.querySelector('.for-you-header');

    navLinks.forEach(link => {
        link.addEventListener('click', (event) => {
            event.preventDefault();
            navLinks.forEach(item => item.classList.remove('active'));
            link.classList.add('active');

            const action = link.dataset.action;
            if (action === 'discover' && heroSection) {
                heroSection.scrollIntoView({ behavior: 'smooth', block: 'start' });
            } else if (action === 'search' && searchInput) {
                searchInput.focus({ preventScroll: false });
            } else if (action === 'trending' && filterSection) {
                filterSection.scrollIntoView({ behavior: 'smooth', block: 'start' });
            }
        });
    });
}

document.addEventListener('DOMContentLoaded', initDashboard);